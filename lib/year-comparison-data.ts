export const getStaffLoadLevel = (totalTtc: number): StaffLoadLevel => {
  if (totalTtc <= 0) return "closed"
  if (totalTtc >= FULL_STAFF_THRESHOLD) return "full"
  if (totalTtc >= REINFORCED_STAFF_THRESHOLD) return "reinforced"
  return "normal"
}

